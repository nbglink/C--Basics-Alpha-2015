using System;


namespace S2FloatOrDouble
{
    class FloatOrDouble
    {
        static void Main()
        {
            double varDouble1 = 34.567839023;

            float varFloat1 = 12.345f;

            double varDouble2 = 8923.1234857;

            float varFloat2 = 3456.091f;

            Console.WriteLine("{0} Double.", varDouble1);
            Console.WriteLine("{0} Float.", varFloat1);
            Console.WriteLine("{0} Double.", varDouble2);
            Console.WriteLine("{0} Float.", varFloat2);
        }
    }
}